# 2) გააკეთეთ 5-5 მაგალითი თთოეულ შედარების ოპერატორზე (>, >=, <, <=, ==, !=), გვერდზე კომენტარის საშვალებით მიუთითეთ თუ რომელ boolean მნიშვნელობას გამოიტანას, აიღეთ მრავალფეროვანი კომბინაციები, შეეცადეთ გქონდეთ ყველა ვარიანტი
# > ოპერატორი
print(5 > 3)  # True
print(2 > 10)  # False
print(-1 > -5)  # True
print(0 > 0)  # False
print(100 > 99)  # True

# >= ოპერატორი
print(5 >= 3)  # True
print(2 >= 10)  # False
print(-1 >= -5)  # True
print(0 >= 0)  # True
print(100 >= 99)  # True

# < ოპერატორი
print(5 < 3)  # False
print(2 < 10)  # True
print(-1 < -5)  # False
print(0 < 0)  # False
print(99 < 100)  # True

# <= ოპერატორი
print(5 <= 3)  # False
print(2 <= 10)  # True
print(-1 <= -5)  # False
print(0 <= 0)  # True
print(99 <= 100)  # True

# == ოპერატორი
print(5 == 3)  # False
print(2 == 2)  # True
print(-1 == -1)  # True
print(0 == 0)  # True
print(99 == 100)  # False

# != ოპერატორი
print(5 != 3)  # True
print(2 != 2)  # False
print(-1 != -5)  # True
print(0 != 1)  # True
print(99 != 100)  # True
